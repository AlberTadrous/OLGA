package semanticstore.ontology.library.generator;

import org.apache.log4j.Logger;
import org.springframework.boot.CommandLineRunner;
import org.springframework.boot.ExitCodeGenerator;
import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;
import org.springframework.context.annotation.ComponentScan;
import springfox.documentation.swagger2.annotations.EnableSwagger2;

@SpringBootApplication
@EnableSwagger2
@ComponentScan(basePackages = {"semanticstore.ontology.library.generator",
    "semanticstore.ontology.library.generator.api",
    "semanticstore.ontology.library.generator.configuration"})
public class Swagger2SpringBoot implements CommandLineRunner {
  final static Logger log = Logger.getLogger(Swagger2SpringBoot.class);

  @Override
  public void run(String... arg0) throws Exception {
    if (arg0.length > 0 && arg0[0].equals("exitcode")) {
      throw new ExitException();
    }
  }

  public static void main(String[] args) throws Exception {
    if (log.isDebugEnabled()) {
      log.debug(" -Main ");
    }

    try {
      new SpringApplication(Swagger2SpringBoot.class).run(args);
    } catch (Exception e) {
      log.error(e);
      System.out.println(e.getMessage());
    }

  }

  static class ExitException extends RuntimeException implements ExitCodeGenerator {
    private static final long serialVersionUID = 1L;

    @Override
    public int getExitCode() {
      return 10;
    }

  }
}
